    # 0. Définition de la fonction d'appel reliée au pushButton_CalculerAires
    def calc_aires(self) -> None:
        cross_section_data = pd.read_csv(
            os.path.join(self.directory_path, "cross_section_data.csv")
        )
        cross_section_data["POINT_Z"] = cross_section_data["POINT_Z"].apply(
            lambda x: round(x, 3)
        )
        dist = cross_section_data["Distance"]
        alti = cross_section_data["POINT_Z"]
        alti_ref = sorted(alti)
        filtrage_aires = float(self.lineEdit_filtrage_aires.text())

        # Groupement des données par transect
        grouped_data = cross_section_data.groupby("x_sec_id")
        # Initialiser une liste pour stocker les résultats par transect
        results_list = []
        # Stocker les résultats dans une liste de dictionnaires
        for x_sec_id, data_group in grouped_data:
            # Supprimer les doublons d'altitudes par transect
            unique_data_group = data_group.drop_duplicates(subset=["POINT_Z"])
            unique_data_group.reset_index(drop=True, inplace=True)

            # Récupérer les altitudes uniques après suppression des doublons ou triples
            alti = unique_data_group["POINT_Z"].values
            alti_ref = sorted(alti)
            dist = unique_data_group["Distance"].values

            # Calculer les aires pour ce transect
            areas_iteration = calculer_aire(dist, alti, alti_ref, filtrage_aires)

            # Stocker les résultats dans une liste de dictionnaires
            for (
                iteration,
                areas,
                distances,
                ref_altitude,
                intersection_indices_list,
            ) in areas_iteration:
                for i, (area, classification, ref_altitude) in enumerate(areas):
                    # Modifier le signe des aires en fonction de la classification
                    modified_area = area if classification == "above" else -area

                    # Dictionnaire pour chaque aire avec les informations requises
                    result_dict = {
                        "x_sec_id": x_sec_id,
                        "iteration": iteration,
                        "area": modified_area,
                        "distance_between_intersections": distances[i],
                        "ref_altitude": ref_altitude,
                        # 'intersection_indices': intersection_indices_list
                    }
                    results_list.append(result_dict)

        # Créer un DataFrame à partir de la liste de dictionnaires
        results_df = pd.DataFrame(results_list)
        df_output_path = os.path.join(self.directory_path, "area_trapezes.csv")
        results_df.to_csv(df_output_path, sep=",", index=False)
        print("Fichier .csv 'area_trapezes.csv' exporté avec succès.")

